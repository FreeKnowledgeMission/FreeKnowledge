// if else statement

// if Joe is the same age as Sam, then display "same age"
// else if Joe is younger than Sam, then display "younger"
// everything else will return older

var Joe = 22
var Sam = 23

if (Joe == Sam) {
    console.log("same age")
}   else if (Joe < Sam){
        console.log("younger")
}   else console.log("older")
